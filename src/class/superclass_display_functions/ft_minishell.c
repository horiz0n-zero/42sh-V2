#include "shell.h"

void		ft_minishell(void)
{
	write(1, "\e[34m", 5);
	write(1, "\n\t\t         _      _     __       ____\n", 39);
	write(1, "\t\t  __ _  (_)__  (_)__ / /  ___ / / /\n", 39);
	write(1, "\t\t /  ' \\/ / _ \\/ (_-</ _ \\/ -_) / / \n", 38);
	write(1, "\t\t/_/_/_/_/_//_/_/___/_//_/\\__/_/_/  \n", 38);
}
